import { createHash } from "node:crypto"
import { mkdir, readFile, writeFile } from "node:fs/promises"
import { dirname } from "node:path"

const sourcePath = "docs/qrspi-contract.md"
const targetPath = "skills/qrspi-design-structure/references/qrspi-design-structure-contract.md"

const source = await readFile(sourcePath, "utf8")
const sourceSha256 = createHash("sha256").update(source).digest("hex")

const generated = `# QRSPI Design and Structure Contract

> Generated by \`scripts/sync-qrspi-skill-reference.mjs\` from
> \`${sourcePath}\` (SHA-256 \`${sourceSha256}\`). Do not edit this file directly.
> This bundled snapshot is normative for its installed skill version. Repository
> maintainers update it with \`bun run skill:sync\` whenever the canonical contract changes.

${source.trimEnd()}
`

if (process.argv.includes("--check")) {
  const existing = await readFile(targetPath, "utf8").catch(() => "")
  if (existing !== generated) {
    console.error(`Bundled QRSPI skill contract is stale. Run: bun run skill:sync`)
    process.exit(1)
  }
} else {
  await mkdir(dirname(targetPath), { recursive: true })
  await writeFile(targetPath, generated)
}
